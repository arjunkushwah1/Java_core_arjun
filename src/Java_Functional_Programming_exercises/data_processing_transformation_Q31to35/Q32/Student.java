package Java_Functional_Programming_exercises.data_processing_transformation_Q31to35.Q32;

public class Student
{
    private String name;
    private int grade;

    public Student(String name, int grade) {
        this.name = name;
        this.grade = grade;
    }

    public String getName() {
        return name;
    }

    public int getGrade() {
        return grade;
    }
}
