package Java_Functional_Programming_exercises.custom_types_nested_streamsQ36to40.Q37;

public class Item
{
    private String name;
    private double price;

    public Item(String name, double price) {
        this.name = name;
        this.price = price;
    }

    public double getPrice() {
        return price;
    }

    public String getName() {
        return name;
    }
}
